    private void removeBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeBtnMouseClicked
        // TODO add your handling code here:
        String[] options = {"No", "Yes"};
        int option = JOptionPane.showOptionDialog(null, "Are you sure to delete?", "Delete room", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);
        
        if (option == 1) {
            ref.child(roomsT.getModel().getValueAt(roomsT.getSelectedRow(), 0).toString()).addListenerForSingleValueEvent(new ValueEventListener() {
                @Override
                public void onDataChange(DataSnapshot dataSnapshot) {
                    dataSnapshot.getRef().removeValue(new DatabaseReference.CompletionListener() {
                        @Override
                        public void onComplete(DatabaseError error, DatabaseReference ref1) {
                            updateTable();
                        }
                    });
                }
            
                @Override
                public void onCancelled(DatabaseError error) {
                    System.out.println("The read failed: " + error.getCode());
                }
            });
        }
    }//GEN-LAST:event_removeBtnMouseClicked

